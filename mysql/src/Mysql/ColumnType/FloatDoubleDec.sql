#演示 decimal 类型、float、double 使用
#创建表
CREATE TABLE t06 (
	num1 FLOAT, 
	num2 DOUBLE, 
	num3 DECIMAL(30,20));
#添加数据
INSERT INTO t06 VALUES(88.12345678912345, 88.12345678912345,88.12345678912345);
SELECT * FROM t06;

#decimal 可以存放很大的数(这串数，bigInt就无法存放，超出了)
#DECIMAL[M,D]  M表示该小数总位数 D表示小数点后位数
#不写 默认 M 为10 D 为0 
#DECIMAL(65) 表示一个65位的数，没有小数位，因此可以存放一些很大的数
CREATE TABLE t07 (
	num DECIMAL(65));
	
INSERT INTO t07 VALUES(8999999933338388388383838838383009338388383838383838383);
SELECT * FROM t07;

